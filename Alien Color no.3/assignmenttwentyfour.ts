/* Turn your if-else chain from Exercise 5-4 into an if-else chain.
• If the alien is green, print a message that the player earned 5 points.
• If the alien is yellow, print a message that the player earned 10 points.
• If the alien is red, print a message that the player earned 15 points.
• Write three versions of this program, making sure each message is printed for the appropriate color alien.*/
let alien_color: string = 'green';

if (alien_color === 'green') {
    console.log("Congratulations! You just earned 5 points for shooting down the green alien.");
} else if (alien_color === 'yellow') {
    console.log("Great job! You just earned 10 points for shooting down the yellow alien.");
} else if (alien_color === 'red') {
    console.log("Wow! You just earned 15 points for shooting down the red alien.");
} else {
    console.log("No points earned. Keep shooting!");
}

